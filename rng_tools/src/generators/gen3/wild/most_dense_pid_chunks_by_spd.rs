use crate::gen3::MAX_PID_MOD_24_CYCLE_COUNT;

#[cfg(test)]
mod test {
    use itertools::Itertools;

    use crate::gen3::calculate_pid_speed;

    use super::*;

    #[ignore]
    #[test]
    fn test_calc_modulo_min_max_by_pid_spd() {
        const CHUNK_SIZE: u32 = 1 << 18;
        const CHUNK_COUNT: usize = 1 << (32 - 18);
        const TRACKED_CHUNK_COUNT_BY_SPD: usize = 5;

        let mut count_by_chunk_and_spd: Vec<[usize; CHUNK_COUNT]> =
            vec![[0; CHUNK_COUNT]; MAX_PID_MOD_24_CYCLE_COUNT + 1];

        // for pid in 0..=0xFFFFFFFF {
        for pid in 0..=0xFFFFFFFFu32 {
            let chunk_id = pid / CHUNK_SIZE;
            let spd = calculate_pid_speed(pid);
            count_by_chunk_and_spd[spd as usize][chunk_id as usize] += 1;
        }

        let best_chunk_by_spd: [[(usize, usize); 5]; MAX_PID_MOD_24_CYCLE_COUNT + 1] =
            count_by_chunk_and_spd
                .iter()
                .map(|chunks| {
                    chunks
                        .iter()
                        .enumerate()
                        .k_largest_by_key(TRACKED_CHUNK_COUNT_BY_SPD, |&(_, &count_in_chunk)| {
                            count_in_chunk
                        })
                        .map(|(idx, &count_in_chunk)| (idx, count_in_chunk))
                        .collect_array()
                        .unwrap()
                })
                .collect_array()
                .unwrap();

        assert_eq!(MOST_DENSE_PID_CHUNKS_BY_SPD, best_chunk_by_spd);
    }
}

